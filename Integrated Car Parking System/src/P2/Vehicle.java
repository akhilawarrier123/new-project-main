package P2;

public class Vehicle {

	private String VehicleLabel;
	private Integer VehicleTimeHrs;
	private Integer VehicleTimeHrsMin;
	private Integer ContactNumber;
	private String VehicleType;

	public String getVehicleLabel() {
		return VehicleLabel;
	}

	public void setVehicleLabel(String vehicleLabel) {
		VehicleLabel = vehicleLabel;
	}

	public Integer getVehicleTimeHrs() {
		return VehicleTimeHrs;
	}

	public void setVehicleTimeHrs(Integer vehicleTimeHrs) {
		VehicleTimeHrs = vehicleTimeHrs;
	}

	public Integer getVehicleTimeHrsMin() {
		return VehicleTimeHrsMin;
	}

	public void setVehicleTimeHrsMin(Integer vehicleTimeHrsMin) {
		VehicleTimeHrsMin = vehicleTimeHrsMin;
	}

	public Integer getContactNumber() {
		return ContactNumber;
	}

	public void setContactNumber(Integer contactNumber) {
		ContactNumber = contactNumber;
	}

	public String getVehicleType() {
		return VehicleType;
	}

	public void setVehicleType(String vehicleType) {
		VehicleType = vehicleType;
	}

}